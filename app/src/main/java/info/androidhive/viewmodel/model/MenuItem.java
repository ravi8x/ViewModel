package info.androidhive.viewmodel.model;

public class MenuItem {
    int id;
    String name;
    String description;
    double price;
    String thumbnail;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public double getPrice() {
        return price;
    }

    public String getThumbnail() {
        return thumbnail;
    }
}
